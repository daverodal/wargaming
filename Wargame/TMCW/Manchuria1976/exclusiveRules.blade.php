    <ol>
        <li class="big"><span> PRC Militia</span>
            <ol>
                <li> Each city owned by the PRC player has an intrinsic militia unit built in.</li>
                <li>Militia units may not move and are destroyed if force to retreat.</li>
                <li>They may attack but may not advance after combat.</li>
                <li>If the Soviet player enters a city they are awarded victory points for the city. The PRC player loses an equal amount of victory points too. The militia unit must have already been eliminated in combat to do this</li>
                <li>If at the end of any phase, a Soviet owned city is left unoccupied by the Soviet player, that city revolts and a militia unit will appear. The points awarded to the soviet player are removed and given back to the PRC player</li>
                <li>Militia units are an exception to the stacking rules. One other unit may stack with them.</li>
            </ol>

        </li>
        <li class="big"><span> PRC Gorillas</span>
            <ol>
                <li> Starting on turn 2, the PRC player may deploy up to 3 gorilla units during their movement phase.</li>
                <li>Gorilla units may be placed ANYWHER within China. This makes them usefull for placing in the soviets rear area and cutting supply lines.</li>
                <li>Gorilla units may not be placed in a ZOC but may move one hex after placement and enter a ZOC.</li>
                <li>A maximum of 3 gorilla units may be on the map at anytime. Gorilla units that are eliminated can be reintroduced the next PRC movement phase.</li>
                <li>Gorilla units may be removed from the map so they may be reintroduced the next turn. Gorilla units removed in this way do not count towards Soviet victory.</li>
                <li>To remove a gorilla unit from the map, simply click on it and then hit the 'x' key or hit the 'x' in the top menu bar.</li>
                <li>Militia units are an exception to the stacking rules. One other unit may stack with them.</li>
            </ol>

        </li>
        <li class="big"><span> PRC Rail movement</span>
            <ol>
                <li> PRC units that start their movement phase on a rail hex man choose to use rail movement.</li>
                <li>In order to use rail movement, one simply clicks on the unit and hits the 'm' key OR clicks on the 'm' on the top menubar.</li>
                <li> Units using rail movement may move 30 hexes, but most only move on rail hexes, and only cross hexsides with a rail connecting the two hexes.</li>
                <li>Units moving my rail movement may NOT enter ZOC's. And hence cannot attack that turn.</li>
            </ol>

        </li>

    </ol>



